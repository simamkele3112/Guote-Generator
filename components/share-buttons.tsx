"use client"

import { useState } from "react"
import { Download, Share2, Check } from "lucide-react"

interface QuoteData {
  id: number
  text: string
  author: string
  category: string
}

interface ShareButtonsProps {
  quote: QuoteData
  onShare?: () => void
}

function drawQuoteImage(quote: QuoteData): Promise<string> {
  return new Promise((resolve) => {
    const canvas = document.createElement("canvas")
    canvas.width = 1080
    canvas.height = 1080

    const ctx = canvas.getContext("2d")!

    // Background gradient
    const gradient = ctx.createLinearGradient(0, 0, 1080, 1080)
    gradient.addColorStop(0, "#1e1b4b")
    gradient.addColorStop(0.4, "#4c1d95")
    gradient.addColorStop(0.7, "#7c3aed")
    gradient.addColorStop(1, "#db2777")
    ctx.fillStyle = gradient
    ctx.fillRect(0, 0, 1080, 1080)

    // Subtle overlay circle top-right
    const radGrad1 = ctx.createRadialGradient(950, 130, 10, 950, 130, 340)
    radGrad1.addColorStop(0, "rgba(236,72,153,0.3)")
    radGrad1.addColorStop(1, "rgba(236,72,153,0)")
    ctx.fillStyle = radGrad1
    ctx.fillRect(0, 0, 1080, 1080)

    // Card background
    ctx.fillStyle = "rgba(255,255,255,0.08)"
    const cardX = 80
    const cardY = 180
    const cardW = 920
    const cardH = 720
    const r = 40
    ctx.beginPath()
    ctx.moveTo(cardX + r, cardY)
    ctx.lineTo(cardX + cardW - r, cardY)
    ctx.quadraticCurveTo(cardX + cardW, cardY, cardX + cardW, cardY + r)
    ctx.lineTo(cardX + cardW, cardY + cardH - r)
    ctx.quadraticCurveTo(cardX + cardW, cardY + cardH, cardX + cardW - r, cardY + cardH)
    ctx.lineTo(cardX + r, cardY + cardH)
    ctx.quadraticCurveTo(cardX, cardY + cardH, cardX, cardY + cardH - r)
    ctx.lineTo(cardX, cardY + r)
    ctx.quadraticCurveTo(cardX, cardY, cardX + r, cardY)
    ctx.closePath()
    ctx.fill()

    // App name top
    ctx.fillStyle = "rgba(255,255,255,0.6)"
    ctx.font = "bold 36px Georgia, serif"
    ctx.textAlign = "center"
    ctx.fillText("✨ QuoteApp", 540, 120)

    // Quote mark
    ctx.fillStyle = "rgba(255,255,255,0.15)"
    ctx.font = "bold 160px Georgia"
    ctx.textAlign = "left"
    ctx.fillText("\u201C", 110, 420)

    // Quote text (word wrap)
    ctx.fillStyle = "rgba(255,255,255,0.95)"
    ctx.font = "italic bold 44px Georgia, serif"
    ctx.textAlign = "center"
    const words = quote.text.split(" ")
    const lines: string[] = []
    let line = ""
    for (const word of words) {
      const testLine = line + word + " "
      if (ctx.measureText(testLine).width > 820 && line !== "") {
        lines.push(line.trim())
        line = word + " "
      } else {
        line = testLine
      }
    }
    lines.push(line.trim())

    const lineHeight = 58
    const totalTextH = lines.length * lineHeight
    const startY = cardY + (cardH - totalTextH) / 2 - 20
    lines.forEach((l, i) => {
      ctx.fillText(l, 540, startY + i * lineHeight)
    })

    // Author
    ctx.fillStyle = "rgba(255,255,255,0.6)"
    ctx.font = "500 34px Georgia, serif"
    ctx.textAlign = "center"
    ctx.fillText("\u2014 " + quote.author, 540, cardY + cardH - 60)

    // Category badge
    ctx.fillStyle = "rgba(255,255,255,0.15)"
    ctx.beginPath()
    ctx.roundRect(440, cardY + 30, 200, 50, 25)
    ctx.fill()
    ctx.fillStyle = "rgba(255,255,255,0.8)"
    ctx.font = "500 26px sans-serif"
    ctx.textAlign = "center"
    ctx.fillText(quote.category.toUpperCase(), 540, cardY + 64)

    // Watermark bottom
    ctx.fillStyle = "rgba(255,255,255,0.35)"
    ctx.font = "24px sans-serif"
    ctx.textAlign = "center"
    ctx.fillText("Generated by QuoteApp", 540, 1040)

    resolve(canvas.toDataURL("image/png"))
  })
}

