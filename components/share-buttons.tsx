          aria-label="Copy quote to clipboard"
          className="flex flex-1 items-center justify-center gap-2 rounded-2xl bg-secondary py-3 text-sm font-semibold text-secondary-foreground border border-border transition-all duration-200 active:scale-95 hover:bg-muted font-sans"
